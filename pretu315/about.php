<h2>About This Project</h2>
<p>
  This project was created for <strong>COMP 3512</strong> at Mount Royal University.
  <br></br>
  It showcases a simple stock portfolio system where users can view company and portfolio information from a database.
</p>

<p>
  The project was built using <strong>PHP</strong> for server-side scripting, <strong>CSS</strong> for styling,
  and <strong>Visual Studio Code (VS Code)</strong> as the main development environment.
  <br></br>
  It runs on <strong>XAMPP</strong>
  <br></br>
  The code is version-controlled and hosted on <strong>GitHub</strong>.
</p>

